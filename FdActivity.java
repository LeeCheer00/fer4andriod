    public void onCameraViewStarted(int width, int height) {
        mGray = new Mat();
        mRgba = new Mat();
        String proto = getPath("swish-fer-cnns.prototxt", this);
        String weights = getPath("swish-fer-cnns.caffemodel", this);
        net = Dnn.readNetFromCaffe(proto, weights);
        Log.i(TAG, "Network loaded successfully");
    }

    public void onCameraViewStopped() {
        mGray.release();
        mRgba.release();
    }

    public Mat onCameraFrame(CvCameraViewFrame inputFrame) {
        // for DNN
        final int IN_WIDTH = 42; // input size for swish-fer-cnns
        final int IN_HEIGHT = 42; // input size for swish-fer-cnns
        //final float WH_RATIO = (float)IN_WIDTH / IN_HEIGHT;
        final double IN_SCALE_FACTOR = 0.007843;
        final double MEAN_VAL = 127.5;
       // final double THRESHOLD = 0.2;

        mRgba = inputFrame.rgba();
        mGray = inputFrame.gray();

        if (mAbsoluteFaceSize == 0) {
            int height = mGray.rows();
            if (Math.round(height * mRelativeFaceSize) > 0) {
                mAbsoluteFaceSize = Math.round(height * mRelativeFaceSize);
            }
            mNativeDetector.setMinFaceSize(mAbsoluteFaceSize);
        }

        MatOfRect faces = new MatOfRect();
        // faces is the object we detected.
        if (mDetectorType == JAVA_DETECTOR) {
            if (mJavaDetector != null)
                mJavaDetector.detectMultiScale(mGray, faces, 1.1, 2, 2, // TODO: objdetect.CV_HAAR_SCALE_IMAGE
                        new Size(mAbsoluteFaceSize, mAbsoluteFaceSize), new Size());
        }
        else if (mDetectorType == NATIVE_DETECTOR) {
            if (mNativeDetector != null)
                mNativeDetector.detect(mGray, faces);
        }
        else {
            Log.e(TAG, "Detection method is not selected!");
        }

        // Draw rectangle around detected object.
      Rect[] facesArray = faces.toArray();
        for (int i = 0; i < facesArray.length; i++) {
            Imgproc.rectangle(mRgba, facesArray[i].tl(), facesArray[i].br(), FACE_RECT_COLOR, 3);

            //we need to classify faces to labels
            //Mat frame = faces;
            //Imagproc.cvtColor(faces, faces, Imagproc.COLOR_GREY2RGB);
            // classId
            Mat subfaces = new Mat(mRgba, facesArray[i]);
            Mat S = new Mat();
            Imgproc.cvtColor(subfaces, S, Imgproc.COLOR_RGBA2RGB);
            Mat blob = Dnn.blobFromImage(S, IN_SCALE_FACTOR, new Size(IN_WIDTH, IN_HEIGHT), new Scalar(MEAN_VAL, MEAN_VAL, MEAN_VAL), /*swapRB*/false, /*crop*/false);
            net.setInput(blob);
            Mat emotions = net.forward();
//            emotions = emotions.reshape(7, 1);
//            int total = (int)emotions.total();
            float[] classvalue = new float[7];
            for (int w = 0; w < emotions.cols(); ++w){
                classvalue[w] = (float)emotions.get(0, w)[0];
            }// 得到classvalue[]


            int classID = (int)getMaxIndex(classvalue)[1];
//            int classID = total - 1;
            String label = classNames[classID];
            // Draw background for label.
            int[] baseLine = new int[1];
            Size labelSize = Imgproc.getTextSize(label, Imgproc.FONT_HERSHEY_SIMPLEX, 1, 1, baseLine);
            Imgproc.rectangle(mRgba, facesArray[i].tl(),
                    new Point(facesArray[i].tl().x + labelSize.width, facesArray[i].tl().y - labelSize.height),
                    new Scalar(255, 255, 255), Core.FILLED);
            // Draw backgroud for label.
            //Imgproc.rectangle(mRgba, new Point( facesArray[i].tl().x, facesArray[i].tl().y - labelSize.height),
            //        new Point(facesArray[i].tl().x + labelSize.width, facesArray[i].tl().y + baseLine[0]),
            //        new Scalar(255, 255, 255), Imgproc.FILLED);
            // Write class name.
            Imgproc.putText(mRgba, label, facesArray[i].tl(), Imgproc.FONT_HERSHEY_SIMPLEX, 1, new Scalar(0, 0, 0, 0));
        }

        return mRgba;
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        Log.i(TAG, "called onCreateOptionsMenu");
        mItemFace50 = menu.add("Face size 50%");
        mItemFace40 = menu.add("Face size 40%");
        mItemFace30 = menu.add("Face size 30%");
        mItemFace20 = menu.add("Face size 20%");
        mItemType   = menu.add(mDetectorName[mDetectorType]);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        Log.i(TAG, "called onOptionsItemSelected; selected item: " + item);
        if (item == mItemFace50)
            setMinFaceSize(0.5f);
        else if (item == mItemFace40)
            setMinFaceSize(0.4f);
        else if (item == mItemFace30)
            setMinFaceSize(0.3f);
        else if (item == mItemFace20)
            setMinFaceSize(0.2f);
        else if (item == mItemType) {
            int tmpDetectorType = (mDetectorType + 1) % mDetectorName.length;
            item.setTitle(mDetectorName[tmpDetectorType]);
            setDetectorType(tmpDetectorType);
        }
        return true;
    }

    private void setMinFaceSize(float faceSize) {
        mRelativeFaceSize = faceSize;
        mAbsoluteFaceSize = 0;
    }

    private void setDetectorType(int type) {
        if (mDetectorType != type) {
            mDetectorType = type;

            if (type == NATIVE_DETECTOR) {
                Log.i(TAG, "Detection Based Tracker enabled");
                mNativeDetector.start();
            } else {
                Log.i(TAG, "Cascade detector enabled");
                mNativeDetector.stop();
            }
        }
    }

    // Upload file to storage and return a path.
    private static String getPath(String file, Context context) {
        AssetManager assetManager = context.getAssets();
        BufferedInputStream inputStream = null;
        try {
            // Read data from assets.
            inputStream = new BufferedInputStream(assetManager.open(file));
            byte[] data = new byte[inputStream.available()];
            inputStream.read(data);
            inputStream.close();
            // Create copy file in storage.
            File outFile = new File(context.getFilesDir(), file);
            FileOutputStream os = new FileOutputStream(outFile);
            os.write(data);
            os.close();
            // Return a path to file which may be read in common way.
            return outFile.getAbsolutePath();
        } catch (IOException ex) {
            Log.i(TAG, "Failed to upload a file");
        }
        return "";
    }
    public static float[] getMaxIndex(float[] arr) {
        if(arr==null||arr.length==0){
            return null;//如果数组为空 或者是长度为0 就返回null
        }
        int maxIndex=0;//假设第一个元素为最大值 那么下标设为0
        float[] arrnew=new float[2];//设置一个长度为2的数组用作记录，第一个元素存储最大值，第二个元素存储下标
        for(int i =0;i<arr.length-1;i++){
            if(arr[maxIndex]<arr[i+1]){
                maxIndex=i+1;
            }
        }
        arrnew[0]=arr[maxIndex];
        arrnew[1]=maxIndex;
        return arrnew;
    }

